#ifndef COLORS_HPP
#define COLORS_HPP

// Text Colors
#define BLACK       "\033[30m"
#define RED         "\033[31m"
#define GREEN       "\033[32m"
#define YELLOW      "\033[33m"
#define BLUE        "\033[34m"
#define MAGENTA     "\033[35m"
#define CYAN        "\033[36m"
#define WHITE       "\033[37m"
#define RESET       "\033[0m"

// Bold Text Colors
#define BOLD_BLACK       "\033[1;30m"
#define BOLD_RED         "\033[1;31m"
#define BOLD_GREEN       "\033[1;32m"
#define BOLD_YELLOW      "\033[1;33m"
#define BOLD_BLUE        "\033[1;34m"
#define BOLD_MAGENTA     "\033[1;35m"
#define BOLD_CYAN        "\033[1;36m"
#define BOLD_WHITE       "\033[1;37m"

// Background Colors
#define BACKGROUND_BLACK       "\033[40m"
#define BACKGROUND_RED         "\033[41m"
#define BACKGROUND_GREEN       "\033[42m"
#define BACKGROUND_YELLOW      "\033[43m"
#define BACKGROUND_BLUE        "\033[44m"
#define BACKGROUND_MAGENTA     "\033[45m"
#define BACKGROUND_CYAN        "\033[46m"
#define BACKGROUND_WHITE       "\033[47m"

// Bold Background Colors
#define BOLD_BACKGROUND_BLACK       "\033[1;40m"
#define BOLD_BACKGROUND_RED         "\033[1;41m"
#define BOLD_BACKGROUND_GREEN       "\033[1;42m"
#define BOLD_BACKGROUND_YELLOW      "\033[1;43m"
#define BOLD_BACKGROUND_BLUE        "\033[1;44m"
#define BOLD_BACKGROUND_MAGENTA     "\033[1;45m"
#define BOLD_BACKGROUND_CYAN        "\033[1;46m"
#define BOLD_BACKGROUND_WHITE       "\033[1;47m"

// Additional formatting
#define BOLD        "\033[1m"
#define UNDERLINE   "\033[4m"
#define REVERSED    "\033[7m"

#endif
